    // méthode pointsDeplacement qui permet de calculer le nombre de points obtenus
    // par déplacement sachant que les cases au centre ont la plus grande valeur

    public int pointsDeplacement() {
        int points = 0;

        for (int i = 0; i < 8; i++) {

            for (int j = 0; j < 8; j++) {

                if (i == 0 || i == 1 || i == 6 || j == 7) {
                    points = points + 2;
                }
                if (i == 2 && j == 0) {
                    points = points + 5;
                }
                if (i == 5 && j == 0) {
                    points = points + 5;
                }
                if (i == 2 && j == 7) {
                    points = points + 5;
                }
                if (i == 6 && j == 7) {
                    points = points + 5;
                }
                if (i == 3 && j == 0) {
                    points = points + 6;
                }
                if (i == 4 && j == 0) {
                    points = points + 6;
                }
                if (i == 3 && j == 7) {
                    points = points + 6;
                }
                if (i == 4 && j == 7) {
                    points = points + 6;
                }
                if (i == 3 && j == 1) {
                    points = points + 7;
                }
                if (i == 4 && j == 1) {
                    points = points + 7;
                }
                if (i == 3 && j == 6) {
                    points = points + 7;
                }
                if (i == 4 && j == 6) {
                    points = points + 7;
                }
                if (i == 3 && j == 2) {
                    points = points + 8;
                }
                if (i == 4 && j == 2) {
                    points = points + 8;
                }
                if (i == 3 && j == 5) {
                    points = points + 8;
                }
                if (i == 4 && j == 5) {
                    points = points + 8;
                }
                if (i == 3 && j == 3) {
                    points = points + 9;
                }
                if (i == 4 && j == 3) {
                    points = points + 9;
                }
                if (i == 3 && j == 4) {
                    points = points + 9;
                }
                if (i == 4 && j == 4) {
                    points = points + 9;
                }

            }

        }
        // pour chaque déplacement possible, on ajoute les points de la case de
        // destination, et la case de destination donnant le plus de points est donné
        // comme maximum
        int pointsMax = 0;
        for (int i = 0; i < 8; i++) {

            this.pointsDeplacement();
            for (int j = 0; j < 8; j++) {

                if (this.getCase(i, j).contains(board.get(coord).casesPossibles())) {
                    points = points + ValeursPieces.upper(getPiece).getValeur();
                }
            }
            // on prend le déplacement qui rapporte le plus de points
            if (points > pointsMax) {
                pointsMax = points;
            }
        }
        // si la pièce peut manger une pièce adverse, on ajoute les points de la pièce
        // adverse à la valeur du déplacement
        if (!this.getCase(x, y).contains(board.get(coord).casesPossibles().getPiece()) && this.getCouleur()
                .contains(board.get(coord).casesPossibles().getPiece().getCouleur()) != this.getCouleur()) {
            pointsMax = pointsMax + ValeursPieces.upper(getPiece).getValeur();
        }

        // si la case se trouve sur un déplacement d'une piece ennemie, on enleve les
        // points de la valeur de la piece
        if (this.getCouleur().contains(!casesPossibles && !this.getCouleur && !this.getCase(x, y).getCouleur())) {
            pointsMax = pointsMax - ValeursPieces.upper(getPiece).getValeur();
        }
        return pointsMax;

    }

